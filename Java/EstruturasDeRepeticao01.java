public class EstruturasDeRepeticao01 {

    public static void main(String [] args){
        
        //While, do while, for

        int count = 0;

        while (count < 11) {
            System.out.println(count);
            count++;
            
        }


        for(int i = 0; i < 10; i++){
            System.out.println("For" + i);

        }
    }
    
}
